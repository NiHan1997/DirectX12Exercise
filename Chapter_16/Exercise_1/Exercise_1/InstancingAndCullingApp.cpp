void InstancingAndCullingApp::BuildRootSignature()
{
	CD3DX12_DESCRIPTOR_RANGE diffuseTexTable;
	diffuseTexTable.Init(D3D12_DESCRIPTOR_RANGE_TYPE_SRV, 7, 2, 0);

	CD3DX12_ROOT_PARAMETER slotRootParameter[4];
	slotRootParameter[0].InitAsShaderResourceView(0, 0);		// InstanceData.
	slotRootParameter[1].InitAsShaderResourceView(1, 0);		// MaterialData.
	slotRootParameter[2].InitAsConstantBufferView(0, 0);		// PassCB.
	slotRootParameter[3].InitAsDescriptorTable(1, &diffuseTexTable, D3D12_SHADER_VISIBILITY_PIXEL);

	auto staticSamplers = GetStaticSamplers();
	CD3DX12_ROOT_SIGNATURE_DESC rootSigDesc(4, slotRootParameter,
		(UINT)staticSamplers.size(), staticSamplers.data(),
		D3D12_ROOT_SIGNATURE_FLAG_ALLOW_INPUT_ASSEMBLER_INPUT_LAYOUT);

	ComPtr<ID3DBlob> serilizedRootSig = nullptr;
	ComPtr<ID3DBlob> errorBlob = nullptr;
	HRESULT hr = D3D12SerializeRootSignature(&rootSigDesc, D3D_ROOT_SIGNATURE_VERSION_1,
		serilizedRootSig.GetAddressOf(), errorBlob.GetAddressOf());
	if (errorBlob != nullptr) OutputDebugStringA((char*)errorBlob->GetBufferPointer());
	ThrowIfFailed(hr);

	ThrowIfFailed(md3dDevice->CreateRootSignature(0, serilizedRootSig->GetBufferPointer(),
		serilizedRootSig->GetBufferSize(), IID_PPV_ARGS(mRootSignature.GetAddressOf())));
}

void InstancingAndCullingApp::BuildDescriptorHeaps()
{
	D3D12_DESCRIPTOR_HEAP_DESC descHeapDesc;
	descHeapDesc.NodeMask = 0;
	descHeapDesc.NumDescriptors = 7;
	descHeapDesc.Flags = D3D12_DESCRIPTOR_HEAP_FLAG_SHADER_VISIBLE;
	descHeapDesc.Type = D3D12_DESCRIPTOR_HEAP_TYPE_CBV_SRV_UAV;
	ThrowIfFailed(md3dDevice->CreateDescriptorHeap(&descHeapDesc,
		IID_PPV_ARGS(mSrvDescriptorHeap.GetAddressOf())));

	CD3DX12_CPU_DESCRIPTOR_HANDLE hDescriptor(mSrvDescriptorHeap->GetCPUDescriptorHandleForHeapStart());

	const std::array<ComPtr<ID3D12Resource>, 7> tex2DArr =
	{
		mTextures["bricksTex"]->Resource,
		mTextures["stoneTex"]->Resource,
		mTextures["tileTex"]->Resource,
		mTextures["crateTex"]->Resource,
		mTextures["iceTex"]->Resource,
		mTextures["grassTex"]->Resource,
		mTextures["defaultTex"]->Resource
	};

	D3D12_SHADER_RESOURCE_VIEW_DESC srvDesc = {};
	srvDesc.Shader4ComponentMapping = D3D12_DEFAULT_SHADER_4_COMPONENT_MAPPING;
	srvDesc.ViewDimension = D3D12_SRV_DIMENSION_TEXTURE2D;
	srvDesc.Texture2D.MostDetailedMip = 0;
	srvDesc.Texture2D.ResourceMinLODClamp = 0.0f;
	
	for (size_t i = 0; i < tex2DArr.size(); ++i)
	{
		srvDesc.Format = tex2DArr[i]->GetDesc().Format;
		srvDesc.Texture2D.MipLevels = tex2DArr[i]->GetDesc().MipLevels;

		md3dDevice->CreateShaderResourceView(tex2DArr[i].Get(), &srvDesc, hDescriptor);
		hDescriptor.Offset(1, mCbvSrvUavDescriptorSize);
	}
}

